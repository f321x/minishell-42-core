#include "minishell.h"


bool	ft_isdelimiter(char c)
{
	if (c == '|' || c == '<' || c == '>' || c == 39 || c == '"' || c == ' '
		|| c == '<' || c == '>' || c == '\0' || c == '$')
		return (true);
	return (false);
}
